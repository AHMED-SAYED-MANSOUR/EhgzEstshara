 <p>
   <b> 1-Rotator Cuff tears .</b>
    The rotator cuff is made up of muscles and four tendons that surround the shoulder joint. Whether from a direct blow or from repetitive motion over time,
     injuries to the rotator cuff cause the tendons to tear and become damaged.
     Causes of a Rotator Cuff Tear
A rotator cuff injury may be caused by:
• A direct blow to the shoulder
• Repetitive overhead motion of the arm, as in swimming, baseball, or tennis
• Chronic degenerative wear and tear on the tendons
• Falling on an outstretched arm
