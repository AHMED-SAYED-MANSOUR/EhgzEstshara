    <h1>Prevenetion</h1>
    <p>
        <b>1-Rotator Cuff Tear Prevention</b>
        Athletes in sports that use a lot of repetitive overhead arm motion are at a higher risk of getting a rotator cuff injury. People over age 40, people who do a lot of heavy lifting, or people with weakened shoulder muscles from inactivity are also at higher risk.
        To help prevent tearing the rotator cuff, avoid heavy lifting and try to give your arm and shoulder a break from repetitive motions. Exercise regularly to keep the muscles around your shoulder joint strong.
        <br>
        <b>2-bone fracture prevention</b>
        Many shoulder fractures and injuries result from accidents that often cannot be prevented. However,
        regular exercise and stretching can improve strength and flexibility in the shoulder to help reduce the risk of injury.
        <br>
        <b>3-lebral tear prevention</b>
        Athletes at an increased risk for labral tears are those that play sports involving repetitive movement of the shoulder or hip, such as:
       • Golf
       • Baseball
       • Tennis
       Other labral tear risk factors include a traumatic injury or a degenerative condition, like osteoarthritis.
       <br>
       <b>4-Bursitis prevention</b>
       Warm up before exercising or before sports or other repetitive movements.
       Start new exercises or sports slowly. Gradually increase the demands you put on your body.
       <br>
       <b>5-Dislocated Shoulder prevention</b>
       A dislocated shoulder can result from a variety of situations that involve impact to the shoulder.
       Common causes of dislocated shoulders include:
       • Any hard fall onto the shoulder.
       • Forceful hitting, lifting, or throwing.
       • A hit to an outstretched arm.
       Athletes at risk of shoulder dislocations include:
       • Football players
       • Hockey players

