  you may feel shoulder pain or experience shoulder instability (the feeling that your shoulder is slipping out of place).
   Labral tears are usually caused by overuse or injury and commonly accompany other injuries to the shoulder.
   <br>
   <b>4-Bursitis symptoms and diagnosis</b>
   <b>
   What Are the Signs and Symptoms of Trochanteric Bursitis?
   </b>
Symptoms of trochanteric bursitis include:
• Pain in the hip when moving the joint.
• Pain when you lie on the affected side of the body.
• Tenderness in the hip.
• Limping.
• Soreness or swelling in the hip.
The pain may spread down your leg toward the knee.
If you've been struggling with hip pain you suspect is bursitis for two weeks, see a sports medicine specialist for an evaluation.
<b>
How Do You Diagnose Trochanteric Bursitis
</b>
A visit to a medical professional can diagnose the bursa inflammation that causes trochanteric bursitis without additional testing. A physical exam combined with a history is usually enough.
As part of the exam, your doctor may ask you to perform a series of range-of-motion activities. This checks how severe your bursitis is. It can also rule out other problems, like a fracture.
However, your doctor may order tests just to make sure there isn't an underlying bone issue. These diagnostic tests can include:
• X-ray
• Bone scan
Though many patients don't need any further diagnostic tests, your doctor may suggest an MRI. This is an advanced imaging test that shows the bursa and soft tissue.
<br>
<b>5-Dislocated shoulder symptoms and diagnosis</b>
<b>
Dislocated shoulder symptoms
</b>
When a shoulder dislocation occurs, the first symptom is an immediate and strong pain.
You may also have other symptoms such as:
• Instability in shoulder.
• Weakness in the shoulder area.
• Inability to move the shoulder.
• Swelling, bruising, numbness, or tingling around the shoulder, or in the arm or fingers.
<b>
Dislocated shoulder diagnosis
</b>
In the event of a partial or complete shoulder dislocation, you should consult a medical expert right away to diagnose the injury.
During the appointment, the doctor will assess your shoulder and symptoms.
He or she may order an imaging test to confirm severity of the shoulder dislocation.

