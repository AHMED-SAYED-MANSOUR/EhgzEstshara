     <br>
Overuse injuries are sports-related microtraumas that result from repetitively using the same parts of the body, usually by overtraining.
A microtrauma is a small injury to a bone, muscle, tendon, or ligament. Examples are tiny tears in muscle fibers, stress on a tendon, or bruising of a bone.
Pain and inflammation are often early symptoms of an overuse injury.
Overuse injuries usually heal quickly in children, but they require complete rest of the injured area while it heals.
Continuing to play with an overuse injury will worsen the injury, potentially leading to chronic pain, stress fractures, or damage to a tendon or ligament.
